#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*ptr;

	if (!s || n < 0)
		return ;
	ptr = (char *)s;
	if (n == 0)
		return ;
	while (n--)
		ptr[n] = 0;
}
