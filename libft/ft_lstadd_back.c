#include "../includes/libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*lst2;

	if (!lst)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	lst2 = ft_lstlast(*lst);
	lst2->next = new;
}
